control 'VCLD-80-000100' do
  title 'The vCenter VAMI service must implement prevent rendering inside a frame or iframe on another site.'
  desc 'Clickjacking, also known as a “UI redress attack”, is when an attacker uses multiple transparent or opaque layers to trick a user into clicking on a button or link on another page when they were intending to click on the top level page. Thus, the attacker is “hijacking” clicks meant for their page and routing them to another page, most likely owned by another application, domain, or both.

Using a similar technique, keystrokes can also be hijacked. With a carefully crafted combination of stylesheets, iframes, and text boxes, a user can be led to believe they are typing in the password to their email or bank account but are instead typing into an invisible frame controlled by the attacker.'
  desc 'check', %q(At the command prompt, run the following command:

# /opt/vmware/cap_lighttpd/sbin/lighttpd -p -f /var/lib/vmware/cap-lighttpd/lighttpd.conf 2>/dev/null|awk '/setenv\.add-response-header/,/\)/'|sed -e 's/^[ ]*//'|grep "X-Frame-Options"

Example result:

"X-Frame-Options" => "Deny",

If the response header "X-Frame-Options" is missing or not configured to "Deny", this is a finding.

Note: The command must be run from a bash shell and not from a shell generated by the "appliance shell". Use the "chsh" command to change the shell for the account to "/bin/bash". Refer to KB Article 2100508 for more details:

https://kb.vmware.com/s/article/2100508)
  desc 'fix', 'Navigate to and open:

/opt/vmware/etc/lighttpd/applmgmt-lighttpd.conf

If header "X-Frame-Options" is not present, add the following line to the end of the file:

setenv.add-response-header += ("X-Frame-Options" => "Deny")

If header "X-Frame-Options" is present and not set to "Deny", update the value as shown below:

"X-Frame-Options" => "Deny",

Note: The last line in the parameter does not need a trailing comma if part of a multi-line configuration.

Restart the service with the following command:

# systemctl restart cap-lighttpd'
